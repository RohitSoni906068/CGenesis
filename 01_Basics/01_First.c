#include <stdio.h> // Preprocessor directive - includes standard input/output library

int main() // Main function - entry point of every C program
{
    // printf() function displays text on the console/screen
    printf("Hello World!\n"); // \n creates a new line after printing

    printf("Welcome to C Programming!\n");
    printf("This is your first step into programming.\n");

    return 0; // Returns 0 to indicate successful program execution
}

/*
Key Learning Points:
1. Every C program must have exactly one main() function
2. #include <stdio.h> is required for input/output operations
3. printf() is used to display output on screen
4. return 0; indicates successful program termination
5. Semicolons (;) end each statement in C
6. Curly braces {} define code blocks
*/