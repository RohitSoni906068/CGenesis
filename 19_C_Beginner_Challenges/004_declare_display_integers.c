// Program to declare two integer variables, assign them values, and display their values.
#include<stdio.h>

int main()
{
    int first,second;

    printf("Enter first number : ");
    scanf("%d",&first);

    printf("Enter second number : ");
    scanf("%d",&second);

    printf("You entered, first number is %d and second number is %d\n",first,second);
    return 0;
}
